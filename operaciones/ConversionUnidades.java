package operaciones;

public class ConversionUnidades {

    public double metrosAKilometros(double m) {
        return m / 1000;
    }

    public double kilometrosAMetros(double km) {
        return km * 1000;
    }

    public double gramosAKilogramos(double g) {
        return g / 1000;
    }

    public double kilogramosAGramos(double kg) {
        return kg * 1000;
    }
}
